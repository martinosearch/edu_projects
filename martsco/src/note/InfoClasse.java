package note;

public class InfoClasse {
	int eff, ontComp, nMoy;
	double Hnote, Lnote, Moy_Cls;
	String percent;

	public void setPercent(String str) {
		this.percent = str;
	}

	public void setNMoy(int nbre) {
		this.nMoy = nbre;
	}

	public void setOntComp(int Comp) {
		this.ontComp = Comp;
	}

	public void setLMoy(double lmoy) {
		this.Lnote = lmoy;

	}

	public void setMoyCls(double moyCls) {
		this.Moy_Cls = moyCls;
	}

	public void setHmoy(double hmoy) {
		this.Hnote = hmoy;
	}

	public void setEff(int effectif) {
		this.eff = effectif;
	}

	public int getEff() {
		return this.eff;
	}

	public int getOntComp() {
		return this.ontComp;
	}

	public int getNMoy() {
		return this.nMoy;
	}

	public String getPercent() {
		return this.percent;
	}

	public double getHnote() {
		return this.Hnote;
	}

	public double getLnote() {
		return this.Lnote;
	}

	public double getMoy_Cls() {
		return this.Moy_Cls;
	}

	public void setHnote(double hnote) {
		Hnote = hnote;
	}

	public void setLnote(double lnote) {
		Lnote = lnote;
	}

	public void setMoy_Cls(double moy_Cls) {
		Moy_Cls = moy_Cls;
	}
}
